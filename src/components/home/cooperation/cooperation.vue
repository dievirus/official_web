            renderCustom: function (swiper, current, total) {
              const activeColor = '1'
              const normalColor = '.3'
              let color = ''
              let paginationStyle = ''
              let html = ''
              for (let i = 1; i <= total; i++) {
                if (i === current) {
                  color = activeColor
